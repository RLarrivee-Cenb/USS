C
C
cccccccccccccccccccccccccccccccccccccccccccccccccccccc
C
C
C
	INTEGER*4	LIB$SYS_TRNLOG
	INTEGER*4	i
	CHARACTER*10 	choice
	INTEGER*4 	status
	CHARACTER*20	highway

	CHARACTER*40    spd_filename
	INTEGER*2	spd_filename_len
	INTEGER*2	spd_fd
	INTEGER*2	access_type
C
	integer		sid
C
C  Case 2 variables
	REAL		get_analog_val
	INTEGER*2	get_analog_stat
C
