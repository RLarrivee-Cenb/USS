	INTEGER*4       gp_sid
	BYTE		gp_bitnum 
	INTEGER*2	digital_val
	INTEGER*2	digital_stat
c
c
	BYTE		extended_flag
	BYTE		gp_bit_num
        BYTE		inactive_flag
c
