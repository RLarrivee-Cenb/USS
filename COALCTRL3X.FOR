C
C
	REAL XOBAS(2),XRESET(2),XGAIN(2),XDERIV(2),XGFAVG(2),
	1	XKOBAS(2),XKRESET(2),XKGAIN(2),XKDERIV(2),XMAXG(2),
	1	XMAXC(2),XOUTPUT(2),XMAXKE(2),XMINGV(2),XKMINUTES(2),
	1	XKEUP(2),XKETIME(2),XPELSTPT(2),XPELTEMP(2),
	1	XBZOBAS(2),XPRESET(2),XPGAIN(2),XBSET(2)
	INTEGER XONGAS(2),XONCOAL(2),XDUST(2),XGNUM(2),XDUSTCOUNT(2),
	1	XDUSTLIM(2)
