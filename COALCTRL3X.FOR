C
C
C  Use the wesapi highway number to form the name of the wesapi logical that
C  names the point directory file and get the file name from the logical
C
           status = LIB$SYS_TRNLOG('WESAPI_PDIR_1',
	1		spd_filename_len,spd_filename,,,)
C
        IF (status .NE. SS$_NORMAL) THEN
	   PRINT *, '  Logical WESAPI_PDIR not defined'
	   stop 'no point dir'
        ENDIF
C
C
C
C  Put a NULL character (0) at the end of the file name string.  This is
C  necessary because the spd library functions expect string arguments
C  to be NULL terminated strings.
	spd_filename(spd_filename_len+1:) = CHAR(0)

C  Call the spd open file function to get access to the point directory file
        access_type = RUNTIME
        spd_fd = SPD_open_file(%ref(spd_filename), access_type)
        IF (spd_fd .LT. 0) THEN
           PRINT *,'  SPD_open_file() Error : ',spd_fd
           STOP
  	ENDIF
C
C
C
C
C
C	  READ IN ANALOG POINT NOS. FROM COALCTRL3X.SRC
C
	OPEN (UNIT=50,FILE='USER_D:[RJM]COALCTRL3X.SRC',STATUS='OLD',
	1	SHARED,READONLY)
C
	JJ=0
	KK=1
C
	DO WHILE (JJ .EQ. 0)
54		READ (50,55,IOSTAT=JJ,err=552) APT
55		FORMAT (A)
		IF (APT(1:1) .LT. '0') GO TO 54
c
c
C  Call the get sid function.  The point name argument must have a NULL (0)
C  at the end.
           status = SPD_get_sid(spd_fd, %ref(apt//char(0)), sid,
	1		gp_sid, gp_bit_num, extended_flag, inactive_flag)
c
	SYSID(KK)=SID
	KK=KK+1
c
	END DO
C
552	CLOSE (50) 	!CLOSE COALCTRL3.SRC FILE
c
C  Close the point directory file 
           status = SPD_close_file(spd_fd)
           IF (status .NE. 0) THEN
    	      PRINT *,'  SPD_close_file() Error : ', status
	      STOP
	   ENDIF
c
c
	status = SHC_open_memory()
c	IF (status .NE. SHC_OK) THEN
c	   PRINT *,' SHC open memory failure - error = ',status
c	   STOP
c	ENDIF
c
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C	ALL SIDS' ARE IN SYSID(25)
C
C	SYSID(1-6) CI;  SYSID(7-16) AI;  SYSID(17) CO;  SYSID(18-19) AO
C
C	SYSID(20,25) PEL SETPT,PEL TEMP, AND COALRATE ANALOGS
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C
C
C
C	SCHEDULING A 2 SECOND WAKEUP REQUEST
C
C       CONVERT ASCII TIME TO BINARY
C
	STATUS=SYS$BINTIM (ALARM,TIMER)   
C
C	SCHEDULE WAKEUP
C
	STATUS=SYS$SCHDWK (,,TIMER,TIMER)
C
C
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C
C
C	FILL THE COAL3 COMMON WITH THE DATA IN COAL3.DAT WHEN THE PROGRAM
C	IS RESTARTED ONLY!
C
C
 	OPEN (UNIT=11,FILE='D_I:COAL3.DAT',STATUS='UNKNOWN',
	1	FORM='UNFORMATTED',RECL=400,ACCESS='DIRECT',
	1	RECORDTYPE='FIXED'
	2	,SHARED,USEROPEN=UFO_OPEN)
C                                    
C
	READ (11,REC=1)OBAS,RESET,GAIN,DERIV,GFAVG,KOBAS,
	1	KRESET,KGAIN,KDERIV,ONGAS,ONCOAL,DUST,GNUM,
	1	DUSTCOUNT,DUSTLIM,MAXG,MAXC,OUTPUT,MAXKE,MINGV,
	1	KMINUTES,KEUP,KETIME,PELSTPT,PELTEMP,
	1	BZOBAS,PRESET,PGAIN,BSET,CMTEMP,CMSETPT,
	1	SPCONTROL,UPLIM,BZRAISE,
	1	BOPTS
C
	CLOSE (11)
C
C
C
	DO 8080 II=1,40
8080		BOPTS(II)=0
C
C
C
C
C
C	toggle contact output no. 1 to keep STEP 3 COAL on control
C       (IT'S THE SAME ONE FOR GAS CONTROL ON 3)
C
1	status = SHC_get_digital_val_stat( SYSID(17), gp_sid, gp_bitnum,
	1		digital_val, digital_stat)
C
			IF (DIGITAL_VAL) THEN
				PUT_DIGITAL_VAL=0
			ELSE
				PUT_DIGITAL_VAL=1
			END IF
C
	POINT_QUALITY=0
	STATUS=SHC_PUT_POINT_QUALITY (SYSID(17),POINT_QUALITY)
c
	status=shc_put_digital_val (sysid(17),put_digital_val)
C
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C	READ IN THE ANALOG VALUES FROM COALCTRL3.SRC EVERY SCAN
C
C
	DO 300 J=7,16
	STATUS=SHC_GET_ANALOG_VAL_STAT (SYSID(J),GET_ANALOG_VAL,
	1	GET_ANALOG_STAT)
C
	GOOD(J)=1
	IF (bitest(get_analog_stat,8)
	1 .or. bitest(get_analog_stat,9) .or. bitest(get_analog_stat,15))
	1	GOOD(J)=0
C
C
	ANVAL(J)=GET_ANALOG_VAL
C
C
300	CONTINUE
C
C
C	read in the PELLET AND COALRATE values
C
	DO 1300 J=20,33
	STATUS=SHC_GET_ANALOG_VAL_STAT (SYSID(J),GET_ANALOG_VAL,
	1	GET_ANALOG_STAT)
C
	GOOD(J)=1
	IF (bitest(get_analog_stat,8)
	1 .or. bitest(get_analog_stat,9) .or. bitest(get_analog_stat,15))
	1	GOOD(J)=0
C
C
	ANVAL(J)=GET_ANALOG_VAL
C
C
1300	CONTINUE
C
C
CCCCCCCCCCCCCCC
C
C
C	READ IN CONTACT VALUES
C
	DO 301 J=1,6
C
	gp_sid=0
	gp_bitnum=0  
	status = SHC_get_digital_val_stat( SYSID(J), gp_sid, gp_bitnum,
	1		digital_val, digital_stat)
C
C
	CXSTS(J)=DIGITAL_VAL
C
C
301	CONTINUE
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       
C
C                      
C
c   patch patch
C
	DO 2000 LINE=1,2	!main loop to do lines 6 and 7
C
	BOBZ=0
C
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C		convert no. of minutes requested to average the COAL LB/HR
C		with: (kminutes) to
C		no. of points. ((kminutes*60 sec.)/2)  where 2 is the scan
C		rate in seconds.
C
C
	NOP=KMINUTES(LINE)*60/2
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C
C
C
	IF (STAT(ONCTL(LINE))) GO TO 2000 	!SEE IF HEATUP IS ON
C
C
C
C
C
C  	CHECK ALL ANALOGS
C
	ANOK=0
C
ccccccccccc   patch 9-11-00
cccc	IF (GOOD(KEAN(LINE))) THEN
C
cccc		KEXIT = ANVAL(KEAN(LINE))
cccc	ELSE
cccc		CALL ALARMS ((KEAN(LINE)),1,LINE)
C
cccc		ANOK=ANOK+1    ! SET IF ANALOG BO
C
cccc	END IF
Ccccccccccc
C
	IF (GOOD(BZAN(LINE))) THEN         	! BURN ZONE temp.
C
		BZTEMP = ANVAL(BZAN(LINE))
		BOPTS(BZAN(LINE))=0
	ELSE
C
C	 if bztemp is b.o. stick 2000 in it to force it over to dust
		BOBZ=1
C
		BOPTS(BZAN(LINE))=1
C
	END IF
C
C
C
	IF (GOOD(GVAN(LINE))) THEN         ! main COAL FEEDER SPEED
C
		VALVP= ANVAL(GVAN(LINE))
		BOPTS(GVAN(LINE))=0
	ELSE
		BOPTS(GVAN(LINE))=1
C
	END IF
C
C
C
C	!! BURN ZONE SETPOINT !!  If "spcontrol(line)" = 0 then use the
C	westinghouse bz setpoint from the coal control, if it's =1 then
C	use the calculated bz setpoint from the gas control program (the
C	point number is in gbzspan(line).  Plus add the degrees input by
C	the operator from bzraise(line).
C
C
	IF (SPCONTROL(LINE) .EQ. 0) THEN
C
C
		IF (GOOD(BZSP(LINE))) THEN         ! BURN ZONE SETPOINT
C
			BZSTPT=ANVAL(BZSP(LINE))
			BOPTS(BZSP(LINE))=0
		ELSE
	       		BOPTS(BZSP(LINE))=1
C
		END IF
C
C
	ELSE
		IF (ANVAL(GBZSPAN(LINE)) .GT. 2200 .AND.
	1	 ANVAL(GBZSPAN(LINE)) .LT. 2700) THEN
			BZSTPT=ANVAL(GBZSPAN(LINE))+abs(BZRAISE(LINE))
			BOPTS(BZSP(LINE))=0
		ELSE
			BOPTS(BZSP(LINE))=1
		END IF
C
C
	END IF
C
C
ccccccc	IF (GOOD(PSPAN(LINE))) THEN         ! PELLET TEMP SETPOINT
C
ccccccc		PELSTPT(LINE)= pspan(line)
		PELSTPT(LINE)= ONCOAL(LINE)
C
ccccc		BOPTS(pspan(LINE))=0
cccccc	ELSE
cccccc		BOPTS(spspan(LINE))=1
C
ccccc	END IF
C
C
C
	IF (GOOD(PELT(LINE))) THEN         ! ACTUAL PELLET TEMP 
C
		PELTEMP(LINE)= ANVAL(PELT(LINE))
		BOPTS(PELT(LINE))=0
	ELSE
		BOPTS(PELT(LINE))=1
C
	END IF
C
C
C
	IF (GOOD(LBCOAL(LINE))) THEN         ! COAL LB/HR
C
		COALRATE= ANVAL(LBCOAL(LINE))
		BOPTS(LBCOAL(LINE))=0
	ELSE
		BOPTS(LBCOAL(LINE))=1
C
	END IF
C
C
C
	IF (GOOD(CMT(LINE))) THEN         ! COAL MILL TEMP.
C
		CMTEMP(LINE)= ANVAL(CMT(LINE))
		BOPTS(CMT(LINE))=0
	ELSE
		BOPTS(CMT(LINE))=1
C
	END IF
C
C
C
	IF (GOOD(CMS(LINE))) THEN         ! COAL MILL SETPOINT
C
		CMSETPT(LINE)= ANVAL(CMS(LINE))
		BOPTS(CMS(LINE))=0
	ELSE
		BOPTS(CMS(LINE))=1
C
	END IF
C
C
C	    check if this is first time ON CONTROL.
	IF (STAT(ONGASCX(LINE))) THEN
C
		IF (.NOT. ONGAS(LINE)) THEN  !NOT on control on prev. scan
			ONGAS(LINE)=1
			OBAS(LINE)=VALVP
			OUTPUT(LINE)=VALVP
			DUST(LINE)=0
			GNUM(LINE)=0
			BZOBAS(LINE)=BZSTPT
			BSET(LINE)=BZSTPT
			GFAVG(LINE)=0
C
			DO 88 I=1,TOTNUM
88				GFNUMS(LINE,I)=0.
		END IF
C
	ELSE
		ONGAS(LINE)=0
		DUST(LINE)=0
C
		BZOBAS(LINE)=BZSTPT
		BSET(LINE)=BZSTPT
		GFAVG(LINE)=COALRATE
C
		OUTPUT(LINE)=VALVP
C
C
C		       !OUTPUT GAS VALVE POSITION TO WESTINGHOUSE!
C
		POINT_QUALITY=0
		STATUS=SHC_PUT_POINT_QUALITY (SYSID(17+line),POINT_QUALITY)
c
		PUT_ANALOG_VALUE=OUTPUT(LINE)
C
		STATUS=SHC_PUT_ANALOG_VAL (SYSID(17+LINE),PUT_ANALOG_VALUE)
C
		GO TO 2000	!DO NEXT LINE
C
	END IF
C
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C	AVERAGE coal lb/hr
C
C
	IF ( .NOT. DUST(LINE)) THEN
C
		IF (GNUM(LINE) .EQ. NOP) GO TO 582
C
		GNUM(LINE)=GNUM(LINE)+1
C
		IF (GNUM(LINE) .LE. 1) THEN
			GFAVG(LINE)=COALRATE
			GFNUMS(LINE,1)=COALRATE
			GO TO 140
		END IF
C
582		IF (GNUM(LINE) .GE. 2) THEN
			DO 120 I=GNUM(LINE),2,-1
				GFNUMS(LINE,I)=GFNUMS(LINE,I-1)
120			CONTINUE
C
			GFNUMS(LINE,1)=COALRATE
		END IF
C
C
		GFAVG(LINE)=0.
C
		L=0
C
		DO 121 I=1,GNUM(LINE)
			IF (GFNUMS(LINE,I) .LE. 0) GO TO 121
C
			GFAVG(LINE)=GFAVG(LINE)+GFNUMS(LINE,I)
			L=L+1
C
121		CONTINUE
C
C
		IF (L .LE. 0) GO TO 140
C
		GFAVG(LINE)=GFAVG(LINE)/L
C
C
C
C
	END IF
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C		bobz=1 means burn zone temp. is b.o.
C
140	IF ((BSET(LINE)-BZTEMP) .GE. DUSTLIM(LINE) .OR. BOBZ .EQ. 1) THEN
C
		IF (.NOT. DUST(LINE)) THEN
			DUSTCOUNT(LINE)=DUSTCOUNT(LINE)+1
			IF (DUSTCOUNT(LINE) .GE. 3) THEN
				DUST(LINE)=1
				KOBAS(LINE)=OUTPUT(LINE)
				IF (GFAVG(LINE) .LE. 0) GFAVG(LINE)=COALRATE
				DO 41 I=1,TOTNUM
41					GFNUMS(LINE,I)=0.
			ELSE
				GO TO 2000	!its not dusty yet, but
C						 dont change valve.
			END IF
		END IF
C
	ELSE
C                 no dust!
C
		IF (DUST(LINE)) THEN
		TEMP_INCREASE(LINE)=0
		DUST(LINE)=0
		DUSTCOUNT(LINE)=0
		OBAS(LINE)=OUTPUT(LINE)
		BZOBAS(LINE)=BSET(LINE)
		GNUM(LINE)=0
C
		DO 499 JJ=1,TOTNUM
499			GFNUMS(LINE,JJ)=GFAVG(LINE)
		END IF
C
C
	END IF
C
C
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C	CHECK THAT BURN ZONE TEMP IS BELOW MAXIMUM (MAXKE)
C
	IF (BZTEMP .GT. MAXKE(LINE)) THEN
C
		BZ_HIGH=.TRUE.
C
	ELSE
C
		BZ_HIGH=.FALSE.
C
	END IF
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C	SEE IF COAL MILL TEMP IS ABOVE COAL MILL SETPOINT
C	IF NOT, SET AN ALARM SO THAT FEED IS NOT INCREASED
C
	MILLTEMP=1	!SET IF TEMP .GE. SETPOINT
C
	IF (CMTEMP(LINE) .LT. CMSETPT(LINE)) THEN
c
c patch patch patch patch 03-12-01
c   (ignore the fact that the milltemp is below the setpoint)
c
ccc		MILLTEMP=0
		MILLTEMP=1
	END IF
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C
	IF (DUST(LINE)) THEN
C
C
C
C
C
C
C
		TEMP_INCREASE(LINE)=TEMP_INCREASE(LINE)+1
		TVAL=KETIME(LINE)*60./2
C
		IF (TVAL .GT. 0) THEN
		IF (TEMP_INCREASE(LINE) .LT. TVAL)
	1 	 GFAVG(LINE)=GFAVG(LINE)+(KEUP(LINE)/(KETIME(LINE)*60./2))
		END IF
C
C
CCCCCCCCC	   IF (GFAVG(LINE) .GT. MAXKE(LINE)) GFAVG(LINE)=MAXKE(LINE)
C
C
		ERROR= ((GFAVG(LINE)-COALRATE)*100)/12000
C
		IF (MILLTEMP .EQ. 0 .AND. ERROR .GT. 0) ERROR=0.
C
C
C		check the coal lb/hr (COALRATE) against the dialed in
C		maximum lb/hr (uplim). Don't allow the coal feeder to
C		speed up any more.
C
		IF (COALRATE .GT. UPLIM(LINE)) ERROR=-2.00
C
C
C		  drop valve if BURN ZONE TEMP is above maximum
		IF (BZ_HIGH) THEN
			ERROR=-2.00
		END IF
C
C
		NBAS= ERROR*KRESET(LINE)+KOBAS(LINE)
C
		KOBAS(LINE)=NBAS
		IF (KOBAS(LINE) .LT. MINGV(LINE)) KOBAS(LINE)=MINGV(LINE)
		IF (KOBAS(LINE) .GT. MAXG(LINE)) KOBAS(LINE)=MAXG(LINE)
C
C
		OUTPUT(LINE)= ERROR*KGAIN(LINE)+NBAS
C
C		
C
C
		IF (OUTPUT(LINE) .GT. MAXG(LINE)) OUTPUT(LINE)=MAXG(LINE)
		IF (OUTPUT(LINE) .LT. MINGV(LINE)) OUTPUT(LINE)=MINGV(LINE)
C
C
		POINT_QUALITY=0
		STATUS=SHC_PUT_POINT_QUALITY (SYSID(17+line),POINT_QUALITY)
c
		PUT_ANALOG_VALUE=OUTPUT(LINE)
C
		STATUS=SHC_PUT_ANALOG_VAL (SYSID(17+LINE),PUT_ANALOG_VALUE)
C
C
C
C
C
C

	ELSE
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C		adjust the BURN ZONE SETPOINT based on the diff. between
C		the pellet temp and pellet setpoint.
C
		ERROR=(PELSTPT(LINE)-PELTEMP(LINE))*100/2500
		NBAS=ERROR*PRESET(LINE)+BZOBAS(LINE)
		BZOBAS(LINE)=NBAS
		BSET(LINE)=ERROR*PGAIN(LINE)+NBAS
c
c
c
C		  keep the calculated bz setpoint + or - 25 degrees of
c		  the dialed in westinghouse bz setpoint
c
		IF (BSET(LINE) .LT. (BZSTPT-25.)) THEN
			BSET(LINE)=BZSTPT-25.
			BZOBAS(LINE)=BZSTPT-25.
		ELSE IF (BSET(LINE) .GT. (BZSTPT+25.)) THEN
			BSET(LINE)=BZSTPT+25.
			BZOBAS(LINE)=BZSTPT+25.
		END IF		
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C
C
C
C
		ERROR= ((BSET(LINE)-BZTEMP)*100)/BZSPAN
C
		IF (MILLTEMP .EQ. 0 .AND. ERROR .GT. 0) ERROR=0.
C
C
C
C
C		check the coal lb/hr (COALRATE) against the dialed in
C		maximum lb/hr (uplim). Don't allow the coal feeder to
C		speed up any more.
C
		IF (COALRATE .GT. UPLIM(LINE)) ERROR= -2.00
C
C
C
C		 drop valve if BZ temp is above maximum
		IF (BZ_HIGH) THEN
			ERROR=-2.00
		END IF
C
C
C
		NBAS= ERROR*RESET(LINE)+OBAS(LINE)
C
		OBAS(LINE)=NBAS
		IF (OBAS(LINE) .LT. MINGV(LINE)) OBAS(LINE)=MINGV(LINE)
		IF (OBAS(LINE) .GT. MAXG(LINE)) OBAS(LINE)=MAXG(LINE)
C
C
		OUTPUT(LINE)= ERROR*GAIN(LINE)+NBAS
C
C		
C
C
		IF (OUTPUT(LINE) .GT. MAXG(LINE)) OUTPUT(LINE)=MAXG(LINE)
		IF (OUTPUT(LINE) .LT. MINGV(LINE)) OUTPUT(LINE)=MINGV(LINE)
C
C
C
		POINT_QUALITY=0
		STATUS=SHC_PUT_POINT_QUALITY (SYSID(17+line),POINT_QUALITY)
c
		PUT_ANALOG_VALUE=OUTPUT(LINE)
		STATUS=SHC_PUT_ANALOG_VAL (SYSID(17+LINE),PUT_ANALOG_VALUE)
C
C
C
C
C
C
C
	END IF
C
C
C
C	Output to westinghouse if coal is on dust or not.  1=dust
C
	PUT_DIGITAL_VAL=0
	IF (DUST(LINE)) PUT_DIGITAL_VAL=1
C
	DIGV=35+(LINE-1)*2
C
	POINT_QUALITY=0
	STATUS=SHC_PUT_POINT_QUALITY (SYSID(DIGV),POINT_QUALITY)
c
	status=shc_put_digital_val (SYSID(DIGV),put_digital_val)
C
C
C
C
C       Output to Westinghouse the coal calculated setpoint
C
	DIGV=31+(LINE-1)*2
	POINT_QUALITY=0
	STATUS=SHC_PUT_POINT_QUALITY (SYSID(DIGV),POINT_QUALITY)
c
	PUT_ANALOG_VALUE=BSET(LINE)
	STATUS=SHC_PUT_ANALOG_VAL (SYSID(DIGV),PUT_ANALOG_VALUE)
C
C       Output to Westinghouse the coal calculated PELLET TEMP setpoint
C
	POINT_QUALITY=0
	STATUS=SHC_PUT_POINT_QUALITY (SYSID(LINE+37),POINT_QUALITY)
c
	PUT_ANALOG_VALUE=PELSTPT(LINE)
	STATUS=SHC_PUT_ANALOG_VAL (SYSID(LINE+37),PUT_ANALOG_VALUE)
C
C
2000	CONTINUE
C
C
C
C
C
C
	CALL SYS$HIBER ()
C
 	GO TO 1
C
C                                                      
	END
