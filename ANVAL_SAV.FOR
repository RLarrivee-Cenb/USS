C
	PROGRAM		ANVAL_SAV
C
C	PROGRAM SAVES ANVAL TABLE TO DISK
C
	IMPLICIT NONE
C
	REAL*4		ANVAL(1200)
	REAL*4		AG3_ANVAL(1000)
	REAL*4		UTL_ANVAL(500)
	REAL*4		CRS_ANVAL(1000)
	REAL*4		CNC_ANVAL(1900)
C
	INTEGER*4	I,J,K,UFO_OPEN
C
	EXTERNAL	UFO_OPEN
C
	COMMON/ANVALTBL/ANVAL
	COMMON/AG3_ANVALTBL/AG3_ANVAL
	COMMON/UTL_ANVALTBL/UTL_ANVAL
	COMMON/CRS_ANVALTBL/CRS_ANVAL
	COMMON/CNC_ANVALTBL/CNC_ANVAL
C
100	CONTINUE
C
	OPEN	(UNIT=1,FILE='ANA:ANVAL.DAT',STATUS='OLD',
	1	RECL=1200,RECORDTYPE='FIXED',FORM='UNFORMATTED',
	1	ACCESS='DIRECT',SHARED,USEROPEN=UFO_OPEN)
C
	WRITE	(1,REC=1)ANVAL
C
	CLOSE	(UNIT=1)
C
	OPEN	(UNIT=1,FILE='ANA:AG3_ANVAL.DAT',STATUS='OLD',
	1	RECL=1000,RECORDTYPE='FIXED',FORM='UNFORMATTED',
	1	ACCESS='DIRECT',SHARED,USEROPEN=UFO_OPEN)
C
	WRITE	(1,REC=1)AG3_ANVAL
C
	CLOSE	(UNIT=1)
C
	OPEN	(UNIT=1,FILE='ANA:UTL_ANVAL.DAT',STATUS='OLD',
	1	RECL=500,RECORDTYPE='FIXED',FORM='UNFORMATTED',
	1	ACCESS='DIRECT',SHARED,USEROPEN=UFO_OPEN)
C
	WRITE	(1,REC=1)UTL_ANVAL
C
	CLOSE	(UNIT=1)
C
	OPEN	(UNIT=1,FILE='ANA:CRS_ANVAL.DAT',STATUS='OLD',
	1	RECL=1000,RECORDTYPE='FIXED',FORM='UNFORMATTED',
	1	ACCESS='DIRECT',SHARED,USEROPEN=UFO_OPEN)
C
	WRITE	(1,REC=1)CRS_ANVAL
C
	CLOSE	(UNIT=1)
C
	OPEN	(UNIT=1,FILE='ANA:CNC_ANVAL.DAT',STATUS='OLD',
	1	RECL=1900,RECORDTYPE='FIXED',FORM='UNFORMATTED',
	1	ACCESS='DIRECT',SHARED,USEROPEN=UFO_OPEN)
C
	WRITE	(1,REC=1)CNC_ANVAL
C
	CLOSE	(UNIT=1)
C
	CALL	LIB$WAIT(120.0)
C
	GOTO 100
C
	CALL 	EXIT
C
	END
