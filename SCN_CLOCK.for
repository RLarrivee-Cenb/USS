C
	PROGRAM	SCN_CLOCK	!SCREENS CLOCK
C
C	LINK SCN_CLOCK,SCN_CLOCK/OPT
C
	IMPLICIT NONE
C
	INCLUDE	'($PRCDEF)'
C
	INTEGER*4	I,J,K,N,UFO_OPEN
	INTEGER*4	CXSTS(100),BIT
	INTEGER*4	SECS,PM2230/81000/,PM2400/86400/
	INTEGER*4	CNTR,FIVMIN/300/,TEMP,ADRS(2)
	INTEGER*4	CX(114)/1197,1199,1200,1192,1193,1194,
	1			1203,1204,1205,1206,1207,1693,
	1			1208,1209,1210,1211,1212,1819,
	1			1213,1214,1215,1216,1217,1842,
	1			1218,1219,1220,1221,1222,1877,
	1			1223,1224,1225,1226,1227,222,
	1			1228,1229,1230,1231,1232,223,
	1			1233,1234,1235,1236,1237,224,
	1			1238,1239,1240,1241,1242,1878,
	1			1243,1244,1245,1246,1247,117,
	1			1248,1249,1250,1251,1252,1879,
	1			75,76,77,79,80,81,83,84,
	1			86,87,227,228,287,332,334,549,
	1			1861,1862,1863,1864,1865,1866,1867,1868,
	1			1869,1870,1871,1872,1873,1874,1875,1876,
	1			1843,1844,1845,1846,1847,1848,1849,1850,
	1			1851,1852,1853,1854,1855,1856,1857,1858/
C
	REAL*4		RSECS,OHRS(2)
C
	CHARACTER*8	TIM/'0 0:5:00'/
	CHARACTER*12	PRGNAM/'SCREENS:SCNZ'/
	CHARACTER*4	PRCNAM/'SCNZ'/
	CHARACTER*12	ENUM
C
	COMMON/CONTACTSCNC/CXSTS
C
	EXTERNAL	UFO_OPEN
C
	RSECS=SECNDS(0.0)	!SECONDS SINCE MIDNIGHT
	SECS=RSECS		!CONVERT TO INTEGER
	TEMP=PM2230-SECS	!CALCULATE FIVE MINUTE SCANS TILL 10:30PM
	IF (TEMP .LT. 0)TEMP=TEMP+PM2400
	CNTR=TEMP/FIVMIN
C
	CALL	SYS$BINTIM(TIM,ADRS)
	CALL	SYS$SCHDWK(,,ADRS,ADRS)	!SCHEDULE 5 MIN WAKEUP
C
200	CONTINUE
C
	OPEN	(UNIT=1,FILE='SCREENS:SCN_CLOCK.DAT',
	1	STATUS='OLD',FORM='UNFORMATTED',ACCESS='DIRECT',
	1	RECL=5,RECORDTYPE='FIXED',SHARED,USEROPEN=UFO_OPEN)
C

	DO 1000	I=1,114	!FOR EACH SCREEN
C
		READ	(1,REC=I)ENUM,OHRS	!OPER TIME FOR THIS SCREEN
C
		J=(ABS(CX(I))/32)+1	!EXTRACT CXSTS LONGWORD
		K=MOD(ABS(CX(I)),32)	!EXTRACT CORRECT BIT
		BIT=JIBITS(CXSTS(J),K,1)	!EXTRACT BIT CONDITION
C
		IF (BIT .EQ. 1)THEN
			OHRS(1)=OHRS(1)+0.08331	!5 MIN OF OPER TIME
			WRITE	(1,REC=I)ENUM,OHRS	!CHANGE RECORD
		END IF
C
1000	CONTINUE
C
	CLOSE	(UNIT=1)
C

	CNTR=CNTR-1	!DECREMENT SCAN COUNTER
C
	IF (CNTR .LE. 0)THEN
		CNTR=288	!RESET SCAN COUNTER
C
		OPEN	(UNIT=1,FILE='SCREENS:SCN_CLOCK.DAT',
	1		STATUS='OLD',FORM='UNFORMATTED',ACCESS='DIRECT',
	1		RECL=5,RECORDTYPE='FIXED',SHARED,USEROPEN=UFO_OPEN)
C
		DO 1200	I=1,114
			READ	(1,REC=I)ENUM,OHRS
			OHRS(2)=OHRS(1)  	!SAVE 24 HR RESULTS
			OHRS(1)=0		!ZERO NEW 24 HR PERIOD
			WRITE	(1,REC=I)ENUM,OHRS
1200		CONTINUE
C
		CLOSE	(UNIT=1)
C
		CALL	SYS$CREPRC(,PRGNAM,,,,,,PRCNAM,%VAL(4),,,
	1		%VAL(PRC$M_DETACH.OR.PRC$M_NOACNT))
C
	END IF
C
	CLOSE	(UNIT=1)
C
	CALL	SYS$HIBER()
	GOTO 200
C
	END
